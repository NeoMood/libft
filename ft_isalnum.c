#include "libft.h"

int	ft_isalnum(int c)
{
	int	alnum;
	int	noalnum;

	alnum = 1;
	noalnum = 0;
	if (c < 48 || (c > 57 && c < 65) || (c > 90 && c < 97) || c > 122)
	{
		return (noalnum);
	}
	return (alnum);
}
